void oledInit()
{
  Wire.begin();					                  // inicializa bus I2C
  oled.begin(SSD1306_SWITCHCAPVCC, 0x3C);	// inicializa pantalla con direccion 0x3C
  oled.clearDisplay();

  oled.drawBitmap(
    (oled.width()  - LOGO_WIDTH ) / 2,
    (oled.height() - LOGO_HEIGHT) / 2,
    logo_bmp, LOGO_WIDTH, LOGO_HEIGHT, 1);
  oled.display();
  delay(2000);

  // Muestra Inicio de OLED
  oled.clearDisplay();
  oled.setCursor(35,10);
  oled.setTextColor(WHITE);	
  oled.print("CARGANDO...");
  oled.display();
  delay(2000);

}

void oledRC()
{
  // FunciÃ³n --> "oled_RC"
  oled.clearDisplay();			
  // TXT ESTATICO
  oled.setCursor(15,2);
  oled.setTextSize(1);			
  oled.setTextColor(WHITE);	
  oled.print("CONTROL AUDUINO:");

  // TXT DIRECCION
  oled.setCursor(0,15);
  oled.print(GIRO);			    
  oled.print(ROT);			    
  oled.print(" grados");			    
  // TXT VELOCIDAD
  oled.setCursor(0,25);
  oled.print(MOVE);			    
  oled.print(VEL);			    
  oled.print(" %");			    
  oled.display();			      
}

